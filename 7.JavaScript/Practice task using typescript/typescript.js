// Typescript Objects
// Basic Object type
var student;
student = {
    name: "Tushar",
    age: 27,
    city: "surat",
};
console.log(student);
var user = {
    Firstname: "Tushar",
    Lastname: "Patel",
    age: 20,
    city: "surat",
};
console.log(user);
var printf = {
    name: "Tushar",
    greet: function () {
        console.log("hello Tushar");
    }
};
console.log(printf);
console.log(printf.greet());
